const PRIORITY_ISO2 = [
  'ua', 'pl', 'de', 'us', 'gb', 'fr', 'es', 'it', 'nl', 'cz', 'at', 'ch', 'ca', 'au', 'il',
];

const flagFiles = readdirSync(flagsDir).filter((f) => f.endsWith('.svg'));
const displayNames = new Intl.DisplayNames(['en'], { type: 'region' });

const entries = flagFiles
  .map((file) => file.replace(/\.svg$/, ''))
  .filter((iso2) => DIAL_CODES[iso2])
  .map((iso2) => {
    let name;
    try {
      const region = iso2.length === 2 ? iso2.toUpperCase() : iso2.split('-')[0].toUpperCase();
      name = displayNames.of(region) ?? iso2.toUpperCase();
      if (iso2.includes('-')) {
        const part = iso2.split('-')[1];
        name = `${name} (${part.toUpperCase()})`;
      }
    } catch {
      name = iso2.toUpperCase();
    }
    return { dialCode: DIAL_CODES[iso2], iso2, name };
  });

const prioritySet = new Set(PRIORITY_ISO2);
const sorted = [
  ...PRIORITY_ISO2.flatMap((iso2) => {
    const match = entries.find((e) => e.iso2 === iso2);
    return match ? [match] : [];
  }),
  ...entries
    .filter((e) => !prioritySet.has(e.iso2))
    .sort((a, b) => a.name.localeCompare(b.name)),
];

const out = `// Generated by scripts/generate-phone-countries.mjs — do not edit manually.
// Re-run: node scripts/generate-phone-countries.mjs

export interface PhoneCountry {
  dialCode: string;
  iso2: string;
  name: string;
}

export const PHONE_COUNTRIES: PhoneCountry[] = ${JSON.stringify(sorted, null, 2)};

export const DEFAULT_PHONE_COUNTRY: PhoneCountry =
  PHONE_COUNTRIES.find((c) => c.iso2 === 'ua') ?? PHONE_COUNTRIES[0]!;

const byIso2 = new Map(PHONE_COUNTRIES.map((c) => [c.iso2, c]));

export function getCountryByIso2(iso2: string): PhoneCountry | undefined {
  return byIso2.get(iso2.trim().toLowerCase());
}

/** Longest-prefix match on digits after "+". */
export function getCountryByDialPrefix(digits: string): PhoneCountry | undefined {
  const normalized = digits.replace(/\\D/g, '');
  if (!normalized) return undefined;

  for (let len = 3; len >= 1; len -= 1) {
    const prefix = normalized.slice(0, len);
    const matches = PHONE_COUNTRIES.filter((c) => c.dialCode === prefix);
    if (matches.length === 1) return matches[0];
    if (matches.length > 1) {
      return matches.find((c) => c.iso2 === 'us') ?? matches[0];
    }
  }

  return undefined;
}
`;

writeFileSync(path.join(root, 'src', 'lib', 'phone', 'countries.ts'), out, 'utf8');
